# Define display_board that accepts a board and prints
# out the current state.
def display_board (gameInputs)
puts " #{gameInputs[0]} | #{gameInputs[1]} | #{gameInputs[2]} "
puts "-----------"
puts " #{gameInputs[3]} | #{gameInputs[4]} | #{gameInputs[5]} "
puts "-----------"
puts " #{gameInputs[6]} | #{gameInputs[7]} | #{gameInputs[8]} "
end
